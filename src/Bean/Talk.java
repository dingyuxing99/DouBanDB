package Bean;

public class Talk {

	private int talkid;
	private String talkanswer;
	private int userid;
	public int getTalkid() {
		return talkid;
	}
	public void setTalkid(int talkid) {
		this.talkid = talkid;
	}
	public String getTalkanswer() {
		return talkanswer;
	}
	public void setTalkanswer(String talkanswer) {
		this.talkanswer = talkanswer;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	

}
